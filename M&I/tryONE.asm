MVI A,8BH //MODE 0 FOR ALL PORT AND A FOR OUTPUT REST INPUT
OUT 15 
TIMER:IN 14 //TIMER FOR WASHING OR AGITATOR MOTOR
MOV E,A 

FILL:IN 13 
ANI 01H 
JNZ WASH 
MVI A,01H 
OUT 12
CALL DELAY_5

WASH:MVI A,02H 
OUT 12 
LOOP:CALL DELAY_1 
DCR E      //E COCNTAIN THE VALUE OF TIMER GIVEN BY USER
JNZ LOOP 
MVI A,00H 
OUT 12 

DRAIN:IN 13 
ANI 02H 
JNZ END 
MVI A,04H 
OUT 12
CALL DELAY_3 
MVI A,11H 
OUT 12
END:HLT 

DELAY_1 :  MVI B, 1C
LOOP1 :  ADI 00
DCR B
JNZ LOOP1
MVI B,1C
LOOP2 : ADI 00
DCR B
JNZ LOOP2
RET

DELAY_5 : ADI 00
SLAY : MVI E,05
CALL DELAY_1
DCR E
JNZ SLAY
RET

DELAY_3 : ADI 00
LAST : MVI E,02
CALL DELAY_1
DCR E
JNZ LAST
RET
